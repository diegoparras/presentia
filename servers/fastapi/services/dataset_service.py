"""
Dataset parsing for data-grounded chart generation (Suite Escriba, Fase 4).

Parses a CSV/TSV/JSON file into a canonical dataset dict that travels with the
presentation: {"columns": [...], "rows": [{col: value}], "table_md": "..."}.
Chart figures generated by the LLM are later validated against these values,
so the dataset is expected to be already aggregated and small (e.g. a
Concilius reconciliation summary), not a raw transactional dump.
"""

import csv
import json
import os
from typing import Any, Dict, List, Optional

DEFAULT_MAX_ROWS = 200

CSV_EXTENSIONS = {".csv", ".tsv"}
JSON_EXTENSIONS = {".json"}
SUPPORTED_EXTENSIONS = CSV_EXTENSIONS | JSON_EXTENSIONS


class DatasetError(Exception):
    pass


def _max_rows() -> int:
    raw = (os.getenv("DATASET_MAX_ROWS") or "").strip()
    try:
        value = int(raw)
        return value if value > 0 else DEFAULT_MAX_ROWS
    except ValueError:
        return DEFAULT_MAX_ROWS


def _coerce_value(value: Any) -> Any:
    """Numbers stay numbers; numeric-looking strings become numbers."""
    if isinstance(value, bool) or value is None:
        return value
    if isinstance(value, (int, float)):
        return value
    if isinstance(value, str):
        text = value.strip()
        if not text:
            return None
        try:
            return int(text)
        except ValueError:
            pass
        try:
            return float(text)
        except ValueError:
            return text
    return str(value)


def parse_csv_text(text: str) -> Dict[str, Any]:
    try:
        dialect = csv.Sniffer().sniff(text[:4096], delimiters=",;\t|")
    except csv.Error:
        dialect = csv.excel
    reader = csv.DictReader(text.splitlines(), dialect=dialect)
    if not reader.fieldnames:
        raise DatasetError("CSV file has no header row")
    columns = [name.strip() for name in reader.fieldnames if name and name.strip()]
    if not columns:
        raise DatasetError("CSV file has no usable columns")
    rows = []
    for raw_row in reader:
        rows.append(
            {col: _coerce_value(raw_row.get(col)) for col in columns}
        )
    return _build_dataset(columns, rows)


def parse_json_text(text: str) -> Dict[str, Any]:
    try:
        payload = json.loads(text)
    except json.JSONDecodeError as exc:
        raise DatasetError(f"Invalid JSON dataset: {exc}") from exc

    if isinstance(payload, dict):
        # Accept {"rows": [...]} wrappers
        payload = payload.get("rows", payload.get("data"))
    if not isinstance(payload, list) or not payload:
        raise DatasetError(
            "JSON dataset must be a non-empty array of objects "
            '(or {"rows": [...]})'
        )
    if not all(isinstance(item, dict) for item in payload):
        raise DatasetError("JSON dataset must be an array of objects")

    columns: List[str] = []
    for item in payload:
        for key in item.keys():
            if key not in columns:
                columns.append(key)
    rows = [
        {col: _coerce_value(item.get(col)) for col in columns} for item in payload
    ]
    return _build_dataset(columns, rows)


def _build_dataset(columns: List[str], rows: List[dict]) -> Dict[str, Any]:
    if not rows:
        raise DatasetError("Dataset has no data rows")
    limit = _max_rows()
    if len(rows) > limit:
        raise DatasetError(
            f"Dataset has {len(rows)} rows; the limit is {limit}. Charts are "
            "grounded on every value, so provide an aggregated summary "
            "(or raise DATASET_MAX_ROWS)."
        )
    dataset = {"columns": columns, "rows": rows}
    dataset["table_md"] = dataset_to_markdown(dataset)
    return dataset


def parse_dataset_file(file_path: str) -> Dict[str, Any]:
    extension = os.path.splitext(file_path)[1].lower()
    if extension not in SUPPORTED_EXTENSIONS:
        raise DatasetError(
            f"Unsupported dataset format '{extension}'. Use CSV, TSV or JSON."
        )
    with open(file_path, "r", encoding="utf-8-sig") as file:
        text = file.read()
    if extension in JSON_EXTENSIONS:
        return parse_json_text(text)
    return parse_csv_text(text)


def _format_cell(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, float):
        return f"{value:g}"
    return str(value)


def dataset_to_markdown(dataset: Dict[str, Any], max_rows: Optional[int] = None) -> str:
    columns = dataset["columns"]
    rows = dataset["rows"][: max_rows or len(dataset["rows"])]
    lines = [
        "| " + " | ".join(columns) + " |",
        "| " + " | ".join("---" for _ in columns) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(_format_cell(row.get(c)) for c in columns) + " |")
    return "\n".join(lines)
